#Python'da Veri Tiplerini araştırınız, her bir veri tipi için kendi cümlelerinizle açıklamalar yazınız.
# Sayısal veri tipleri
# İnteger : Matematiksel işlemlerde kullanılan ama ondalık değil tamsayı halinde olan veri tipidir. Örnek:1,2,-2,-5
# Float   : Matematiksel işlemlerde kullanılan ondalık sayıları da içeren veri tipidir. Örnek:3.6,8.5,4.7
# Complex : Matematiksel işlemlerde kullanılan karmaşık yapılar oluşturabildiğin veri tipidir. Örnek: 1a,3c

#Metinsel veri tipleri
# string  :Metinsel bir veri tipidir matematiksel bir işlem yapılamaz Örnek:"Ali", "Veli"

# Mantıksal veri tipleri
# Boolean : Doğru ve yanlış değerlerini bulmak için kullanılan veri tipidir Örnek: 1=1:True, 1=2:False

# Dizi veri tipleri
# List    : Bir çok veriyi içerisinde barındırabilen veri tipidir. Örnek: x=(1,2,3,4), y=("Ali", "Veli","Ahmet")


